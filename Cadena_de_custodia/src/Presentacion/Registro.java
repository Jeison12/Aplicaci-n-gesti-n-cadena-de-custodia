    private boolean validarUsuario() {
        if (!validarForma()) {
            Utils.mostrarMensaje("Forma invalida");
            return false;
        }
        if (existeEnBd()) {
            Utils.mostrarMensaje("El usuario ya existe en la BD");
            return false;
        }
        if (!tieneClaveEmpresa()) {
            Utils.mostrarMensaje("La clave de empresa no es valida");
            return false;
        }
        asignarObjetoPersona();
        return true;
    }

    private boolean validarForma() {
        int fallas = 0;
        if(!Utils.vallidarTamanioMinimo(txtUser, 4)){
            Utils.mostrarMensaje("Usuario debe ser mas largo");
            fallas++;
        }
        if(!Utils.vallidarTamanioMinimo(txtPass, 4)){
            Utils.mostrarMensaje("Password debe ser mas larga");
            fallas++;
        }
        if(!Utils.vallidarTamanioMinimo(txtClave, 4)){
            Utils.mostrarMensaje("Clave de empresa debe ser mas larga");
            fallas++;
        }
        if(!Utils.vallidarTamanioMinimo(txtNombres, 3)){
            Utils.mostrarMensaje("Nombres debe ser mas largo");
            fallas++;
        }
        if(!Utils.vallidarTamanioMinimo(txtApellidos, 3)){
            Utils.mostrarMensaje("Apellidos debe ser mas largo");
            fallas++;
        }
        return fallas==0;        
    }

    private boolean tieneClaveEmpresa() {
        return empresaLog.validarClaveEmpresa(txtClave.getText());
    }

    private boolean existeEnBd() {
        return personalog.existeEnBd(txtUser.getText());        
    }    

    

    private void asignarObjetoPersona() {
        personalog.getObjPersona().setUsuario(txtUser.getText());
        personalog.getObjPersona().setPass(txtPass.getText());
        personalog.getObjPersona().setClaveEmpresa(txtClave.getText());
        personalog.getObjPersona().setNombres(txtNombres.getText());
        personalog.getObjPersona().setApellidos(txtApellidos.getText());
        personalog.getObjPersona().setIdentificacion(txtIdentificacion.getText());
        personalog.getObjPersona().setCelular(txtCelular.getText());
        personalog.getObjPersona().setTelefono(txtTelefono.getText());
        personalog.getObjPersona().setEmail(txtEmail.getText());
    }

    public Usuario getUsuario(){
        return new Usuario(personalog.getObjPersona().getId(), personalog.getObjPersona().getNombres(), personalog.getObjPersona().getApellidos());
    }
    
