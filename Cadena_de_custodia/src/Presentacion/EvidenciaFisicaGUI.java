    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (validar()) {
            asignarObjetoEvidencia();
            if (evidenciaLog.nuevaEvidencia()) {
                System.out.println("Agregada a BD");
                estado = true;
                this.dispose();
            } else {
                System.out.println("No agregada a BD");
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private boolean validar() {
        int fallas = 0;
        if (!Utils.vallidarTamanioMinimo(txtNombre, 4)) {
            System.out.println("Nombre mas largo");
            fallas++;
        }
        if (!Utils.vallidarTamanioMinimo(arFuncion, 10)) {
            System.out.println("Desc mas larga");
            fallas++;
        }
        return fallas == 0;
    }

    private void asignarObjetoEvidencia() {        
        evidenciaLog.setObjEvidenciaFisica(new EvidenciaFisica(txtIp.getText(), txtMarca.getText(), txtModelo.getText(), txtRam.getText(), txtDD.getText(), txtCPU.getText(), arFuncion.getText(), arObservaciones.getText(), info.getIdCaso(), info.getIdPersona(), txtRef.getText(), txtNombre.getText(), arDescripcion.getText(), arObservaciones.getText()));
    }

    public EvidenciaFisica getNewEvidencia() {
        return evidenciaLog.getObjEvidenciaFisica();
    }

