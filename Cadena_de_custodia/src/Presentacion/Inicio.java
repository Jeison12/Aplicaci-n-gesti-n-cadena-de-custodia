    private boolean validar(){
        if(!validarForm()){            
            return false;
        }
        if(!validarUsuario()){
            System.out.println("Usuario y contrasenia equivocados");
            return false;
        }
        return true;
    }
    
    private boolean validarForm(){
        int fallas = 0;
        if(!Utils.vallidarTamanioMinimo(txtUsuario, 3)){
            System.out.println("Usuario muy corto");
            fallas++;            
        }
        if(!Utils.vallidarTamanioMinimo(txtPass, 3)){
            System.out.println("Pass muy corta");
            fallas++;
        }
        return true;
    }
    private boolean validarUsuario(){  
        usuario = usuariobd.validarUsuario(txtUsuario.getText(), txtPass.getText());
        return usuario!=null;
    }
    public Usuario getUsuario(){
        return usuario;
    }
    
    
