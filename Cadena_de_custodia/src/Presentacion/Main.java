    private void cargarPerfil() {
        usuario = inicioGUI.getUsuario();
        menUsuario.setText(usuario.getNombre());
    }

    private void cargarModelo() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Caso");
        modelo.addColumn("Detalle");
        modelo.addColumn("Rol");
        tblCasos.setModel(modelo);
    }

    private void cargarCasos() {
        String fila[] = new String[3];
        DefaultTableModel model = (DefaultTableModel) tblCasos.getModel();

        for (Caso caso : casolog.listarCasos(usuario.getId_persona())) {
            fila = new String[3];
            //TODO: LLenar tabla 
            fila[0] = caso.getReferencia();
            fila[1] = caso.getDetalles();
            fila[2] = caso.getRol();
            model.addRow(fila);
        }
    }

    private void revisarSolicitudesDeCaso() {
        int solicitudes = casolog.tieneSolicitudes(usuario.getId_persona());

        if (solicitudes > 0) {
            mitSolicitudes.setText("(" + solicitudes + ") Solicitud de caso");
            menCasos.setText("Casos *("+solicitudes+")");
        } else {
            mitSolicitudes.setText("Solicitud de caso");
            menCasos.setText("Casos");
        }

    }

