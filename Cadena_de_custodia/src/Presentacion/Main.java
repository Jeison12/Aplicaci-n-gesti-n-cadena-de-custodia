    private void solicitudCerrada() {
        //cargar caso del id retornado 
        if (solicitudes.isAceptada()) {
            Caso caso = casolog.getCaso(solicitudes.getIdCasoAceptado());
            caso.setRol("INVESTIGADOR");
            if (caso.getId() > 0) {
                modelo.addRow(new String[]{caso.getReferencia(),caso.getDetalles(),caso.getRol()});
            }
        }
        this.show();
    }

